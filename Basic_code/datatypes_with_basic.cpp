#include <iostream>
using namespace std;

int main()
{
    //integer value
    int a = 5;
    int b = 2;

    //floating value
    float c = 2.6;
    float d = 7.3;

    //double value
    double e = 12.4;
    double f = 45.89; 

    //character value
    char g = 'a';
    char h = 'm';

    //boolean value
    bool pass = true;
    bool fail = false;

    //string value
    string name = "Tushar";
    string number = "45";

    std::cout<<"integer value :\n" <<"a = "<<a <<"  b = " <<b <<"\n" <<std::endl;
    std::cout<<"floating value :\n" <<"c = "<<c <<"  d = " <<d <<"\n" <<std::endl;
    std::cout<<"double value :\n" <<"e = " <<e <<"  f = " <<f <<"\n" <<std::endl;
    std::cout<<"character value :\n" <<"g = " <<g <<"  h = " <<h <<"\n" <<std::endl;
    std::cout<<"boolean value :\n"   <<"pass = "<<pass <<"  fail = " <<fail <<"\n" <<std::endl;
    std::cout<<"string value :\n"  <<"name = " <<name <<"  number = " <<number <<"\n" <<std::endl;
    return 0;
 }